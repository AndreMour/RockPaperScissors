package Jogo.model;

public class Jogada {

	private String name;
	private int jogar;
	
	public Jogada(String name, int jogar) {
		super();
		this.name = name;
		this.jogar = jogar;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getJogar() {
		return jogar;
	}

	public void setJogar(int jogar) {
		this.jogar = jogar;
	}
	
	
	
	
	
	
	
}
